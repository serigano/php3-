<?php

for ($i = 1; $i < 10; $i ++){
    for ($j = 8; $j >= $i; $j --){
        echo "*";
    }
    for ($k = 1; $k <= $i; $k ++){
        echo $k;
    }
    for ($l = 8; $l >=11 - $k; $l -- ){
        echo $l - 9 + $i;
    }
    
    echo "<br>";
}
